% 返回对应（信用级别, 利率）时的用户损失率
function res = l(x,r)
    if x == 1
        m = [-21.64917258246196, 0.8392788909925697, 0.9964462582508753];
    elseif x == 2
        m = [-19.620654904997046, 0.7490310960146717, 0.9835281281694129];
    else
        m = [-18.040400095336427, 0.71854979175074, 1.0119610930533];
    end
    res = exp(m(1) * r +m(2)) + m(3);
end